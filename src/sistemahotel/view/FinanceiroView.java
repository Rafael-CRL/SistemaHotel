    private void BtnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnRegistrarActionPerformed
        try {
            String tipo = CbxTipo.getSelectedItem().toString();
            double valor = Double.parseDouble(TxtValor.getText());
            String descricao = TxtADescricao.getText();
            
            //metodo para converte a data para JtextField
            java.util.Date dataUtil = new SimpleDateFormat("dd/MM/yyyy").parse(TxtData.getText());
            java.sql.Date data = new java.sql.Date(dataUtil.getTime());
            
            FinanceiroController controller = new FinanceiroController();
            controller.registrarPagamento(data, tipo, valor, descricao);
            JOptionPane.showMessageDialog(null, "Transação realizada com sucesso");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao registrar transação: " + e);
        }
    }//GEN-LAST:event_BtnRegistrarActionPerformed

