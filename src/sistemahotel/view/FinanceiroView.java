    private void BtnBuscarSaldoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnBuscarSaldoActionPerformed
        try {
        java.util.Date dataUtil = new SimpleDateFormat("dd/MM/yyyy").parse(TxtDataDoSaldo.getText());
        java.sql.Date data = new java.sql.Date(dataUtil.getTime());

        FinanceiroController controller = new FinanceiroController();
        double saldo = controller.calcularSaldoDoDia(data);

        TxtSaldoTotal.setText(String.format("R$ %.2f", saldo));
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erro ao calcular saldo: " + e);
    }
    }//GEN-LAST:event_BtnBuscarSaldoActionPerformed

