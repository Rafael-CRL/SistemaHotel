    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        try {
            // Coleta todos os dados dos campos da tela
            String nome = txtNome.getText();
            String cpf = txtCpf.getText();
            String cargo = txtCargo.getText();
            BigDecimal salario = new BigDecimal(txtSalario.getText());
            String login = txtLogin.getText();
            String senha = new String(txtSenha.getPassword());
            String email = txtEmail.getText();

            // CORREÇÃO 1: Lendo do campo de telefone correto
            String telefone = txtTelefone.getText();

            String status = (String) cmbStatus.getSelectedItem();

            // CORREÇÃO 2: Lendo a data do campo de texto, em vez de usar a data atual
            DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
            LocalDate dataAdmissao = LocalDate.parse(txtDataAdmissao.getText(), formatter);

            // Se this.funcionario não é nulo, estamos no MODO EDIÇÃO
            if (this.funcionario != null) {
                // Atualiza o objeto existente com os novos dados
                this.funcionario.setNome(nome);
                // O CPF não é atualizado pois é um identificador único
                this.funcionario.setCargo(cargo);
                this.funcionario.setSalario(salario);
                this.funcionario.setLogin(login);
                this.funcionario.setEmail(email);
                this.funcionario.setTelefone(telefone);
                this.funcionario.setStatus(status);
                this.funcionario.setDataAdmissao(dataAdmissao);

                // Se o campo senha foi preenchido, atualiza a senha
                if (!senha.isEmpty()) {
                    this.funcionario.setSenha(senha); // Futuramente, criptografar aqui
                }

                funcionarioController.atualizarFuncionario(this.funcionario);
                JOptionPane.showMessageDialog(this, "Funcionário atualizado com sucesso!");

            } else { // Se não, estamos no MODO ADIÇÃO
                boolean sucesso = funcionarioController.cadastrarFuncionario(nome, cpf, cargo, dataAdmissao, salario, login, senha, email, telefone);
                if (sucesso) {
                    JOptionPane.showMessageDialog(this, "Funcionário cadastrado com sucesso!");
                } else {
                    JOptionPane.showMessageDialog(this, "Falha ao cadastrar funcionário. Verifique os dados.");
                }
            }

            // Após salvar, atualiza a tabela na tela principal e fecha o formulário
            this.viewPai.atualizarTabela();
            this.dispose();

        } catch (java.time.format.DateTimeParseException e) {
            JOptionPane.showMessageDialog(this, "Formato de data inválido. Use dd/MM/yyyy.", "Erro de Formato", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao salvar funcionário: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

