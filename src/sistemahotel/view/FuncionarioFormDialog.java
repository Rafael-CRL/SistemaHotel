    private void preencherFormulario() {
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");

        txtNome.setText(funcionario.getNome());
        txtCpf.setText(funcionario.getCpf());
        txtCargo.setText(funcionario.getCargo());
        txtSalario.setText(funcionario.getSalario().toString());
        txtLogin.setText(funcionario.getLogin());
        txtEmail.setText(funcionario.getEmail());
        txtTelefone.setText(funcionario.getTelefone());
        cmbStatus.setSelectedItem(funcionario.getStatus());
        
        if (funcionario.getDataAdmissao() != null) {
            txtDataAdmissao.setText(funcionario.getDataAdmissao().format(formatter));
        }

        // O campo de senha é intencionalmente deixado em branco por segurança.
        txtSenha.setText("");
        
        // Desabilita a edição do CPF no modo de edição.
        txtCpf.setEditable(false);
    }
